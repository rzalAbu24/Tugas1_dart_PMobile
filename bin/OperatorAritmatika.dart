// Operator Aritmatika
void main() {
  // Penambahan +
  var penambahan = 5 + 10;
  print(penambahan);

  // Pengurangan -
  var pengurangan = 15-2;
  print(pengurangan);

  // Bilangan negatif
  var negatif = -2;
  print(negatif);

  // Bilangan Positif
  var positif = 4;
  print(positif);

  // Perkalian *
  var perkalian = 4 * 6;
  print(perkalian);

  // Pembagian Hasil Double
  var pembagian1 = 10 / 4;
  print(pembagian1);

   // Pembagian Hasil Int
  var pembagian2 = 10 ~/ 4;
  print(pembagian2);

  // Sisa Bagi
  var sisa = 12 % 5;
  print(sisa);
}