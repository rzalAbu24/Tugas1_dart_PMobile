// Switch case
void main() {
  var nilai = 'E';

  switch (nilai) {
    case 'A':
    print('Anda LULUS dengan sangat baik');
    break;

    case 'B':
    print('Anda LULUS dengan baik');
    break;

    case 'C':
    print('Anda LULUS');
    break;

    case 'D':
    print('Anda TIDAK LULUS');
    break;
    
    default:
    print('Mungkin anda salah jurusan');
  }
}